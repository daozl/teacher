#include <stdio.h>
#include <stdlib.h>

int main()
{
	int arr[2][3] = {1,2,3,4};
	int i,j;
	
	printf("arr = %p\n",arr);
	printf("sizeof(arr) = %d\n",sizeof(arr));

	for(i = 0 ; i < 2; i++)
	{
		for(j = 0 ; j < 3; j++)
		{
			printf("%p -> %d\n",&arr[i][j],arr[i][j]);
		}
		printf("\n");
	}


	exit(0);
}


