#include <stdio.h>
#include <stdlib.h>


int main()
{
	int s;

	scanf("%d",&s);

	if(s < 0 || s > 100)
		return -1;

	switch(s/10)
	{
		case 10:
		case 9:	
			putchar('A');
			break;
		case 8:
            putchar('B');
            break;
		case 7:
            putchar('C');
            break;
		case 6:
            putchar('D');
            break;
		case 5:
		case 4:
		case 3:
		case 2:
		case 1:
		case 0:
			putchar('E');
			break;
		default:
			break;

	}

	exit(0);
}


